// Funções matematicas

// Sqrt //Pow

//Raiz quadrada de 25
console.log(Math.sqrt(25));// $

//Potencia
console.log(Math.pow(3, 2));// 9
console.log(Math.pow(4, 3));// 64

//Raisz cubica
console.log(Math.pow(27, (1/3)));//27^(1/3) = 3

//Arredondamento
//Math.round (arredonda para o mais proximo)
